function moverPaises(){
    console.log(posSlider);
    var paisActual = $(".slider-element.activo");
    var paisActualI = paisActual.index()+1;
    var paisSiguiente = $(".slider-element:nth-child("+(paisActualI+1)+")");
    var paisAnterior = $(".slider-element:nth-child("+(paisActualI+-1)+")")
    var avanzar = $(this).hasClass("btn-right");

    var despl= $(".slider-cont").width();
    
    var limite=(cantidad-1)*-despl;
   
if(avanzar){
    console.log(limite);
if (posSlider>limite){
    console.log(posSlider);
    posSlider-=despl;
    console.log(posSlider);
    $(".slider-wrapper").css("transform","translateX("+posSlider+"px)");
    
    paisActual.removeClass("activo");
    paisSiguiente.addClass("activo");

   
}
$(".info-cont").fadeOut(300,function(){
    cambiarInfoPais();
    $(".info-cont").fadeIn(300);
});
}
else{
    if (posSlider<0){
        console.log(posSlider);
        posSlider+=despl;
        console.log(posSlider);
        $(".slider-wrapper").css("transform","translateX("+posSlider+"px)");
        
        paisActual.removeClass("activo");
        paisAnterior.addClass("activo");
    
   
    }
    $(".info-cont").fadeOut(300,function(){
        cambiarInfoPais();
        $(".info-cont").fadeIn(300);
    });
}


}


